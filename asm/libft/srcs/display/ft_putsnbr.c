#include "libft.h"

void	ft_putsnbr(short int n)
{
	if (n == -32768)
	{
		write(1, "-3", 2);
		n = 2768;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (9 < n)
	{
		ft_putsnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
	if (n >= 0 && n <= 9)
		ft_putchar(n + 48);
}
