#include "libft.h"

void	ft_putimnbr(intmax_t n)
{
	if (n == LLONG_MIN)
	{
		write(1, "-9223372036854775808", 20);
		return ;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (9 < n)
	{
		ft_putimnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
	if (n >= 0 && n <= 9)
		ft_putchar(n + 48);
}
