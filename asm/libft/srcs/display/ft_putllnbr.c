#include "libft.h"

void	ft_putllnbr(long long int n)
{
	if (n == LLONG_MIN)
	{
		write(1, "-9", 2);
		n = 223372036854775808;
	}
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
	}
	if (9 < n)
	{
		ft_putllnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
	if (n >= 0 && n <= 9)
		ft_putchar(n + 48);
}
