#include "libft.h"

void	ft_putunbr(uintmax_t n)
{
	if (9 < n)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + 48);
	}
	if (n <= 9)
		ft_putchar(n + 48);
}
