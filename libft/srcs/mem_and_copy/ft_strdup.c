#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*s2;

	i = ft_strlen(s);
	if (!(s2 = (char *)malloc(sizeof(char) * i + 1)))
		return (NULL);
	i = 0;
	while (s[i])
	{
		s2[i] = s[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
