#include "libft.h"

int		reverse_int32(int c)
{
	return (((c >> 24) & 0x000000ff) |
				((c << 24) & 0xff000000) |
				((c << 8) & 0x00ff0000) |
				((c >> 8) & 0x0000ff00));
}

short	reverse_int16(short c)
{
	return (((c << 8) & 0xff00) | ((c >> 8) & 0x00ff));
}
