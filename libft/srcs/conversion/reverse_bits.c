#include "libft.h"

int		reverse_int32(int c)
{
	return ((c >> 24) |
				(c << 24) |
				((c << 8) & 0x00ff0000) |
				((c >> 8) & 0x0000ff00));
}

short	reverse_int16(short c)
{
	return (c >> 8 | c << 8);
}
